    private void btNegativoPositivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNegativoPositivoActionPerformed
        if(calculadora.getQualDigito()==1){
            calculadora.setValor1((calculadora.getValor1()*-1));
            EpDisplay.setText(""+calculadora.getValor1());
        }else if(calculadora.getQualDigito()==2){
            calculadora.setValor2((calculadora.getValor2()*-1));
            EpDisplay.setText(""+calculadora.getValor1());
        }
        
    }//GEN-LAST:event_btNegativoPositivoActionPerformed

